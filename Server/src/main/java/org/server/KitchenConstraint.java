package org.server;

public class KitchenConstraint {
    public String userEmail;
    public Integer burner;
    public Integer pot;
    public Integer pan;
    public Integer knife;
    public Integer cuttingBoard;
    public Integer oven;
    public Integer microwave;

    public KitchenConstraint() {
        this.userEmail = "";
        this.burner = 0;
        this.pot = 0;
        this.pan = 0;
        this.knife = 0;
        this.cuttingBoard = 0;
        this.oven = 0;
        this.microwave = 0;
    }
}
