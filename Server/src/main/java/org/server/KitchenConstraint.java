package org.server;

public class KitchenConstraint {
    public String userEmail;
    public Integer pot;
    public Integer pan;
    public Integer bowl;
    public Integer cuttingBoard;
    public Integer oven;

    public KitchenConstraint() {
        this.userEmail = "";
        this.pot = 0;
        this.pan = 0;
        this.bowl = 0;
        this.cuttingBoard = 0;
        this.oven = 0;
    }
}